-- Environment drift repair (idempotent).
--
-- Some remote environments (e.g. gk-crm-dev) recorded 20260625120000 (gk_catalogs)
-- and 20260625120100 (gk_orgs) in schema_migrations while an EARLIER version of
-- those files was applied, so later edits to them never reached the DB. This
-- migration reconciles any such environment to the current schema. Every
-- statement is guarded (IF NOT EXISTS / ON CONFLICT / DROP IF EXISTS), so on a
-- fresh database that already ran the current gk_catalogs/gk_orgs it is a no-op.

-- companies.slug (gk_orgs) -----------------------------------------------------
alter table public.companies add column if not exists slug text;
create unique index if not exists companies_slug_key on public.companies (slug);

-- companies.kind: current schema has NO check (source of truth = company_org_types).
-- Drop the stale enum check some envs still carry (pro_club/media vocabulary).
alter table public.companies drop constraint if exists companies_kind_check;

-- org_types catalog (gk_catalogs) ----------------------------------------------
create table if not exists public.org_types (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null,
  created_at timestamptz not null default now()
);

insert into public.org_types (code, name) values
  ('club','Club'),('league','League'),('competition','Competition'),
  ('federation','Federation'),('national_team','National Team'),
  ('broadcaster','Broadcaster'),('rights_holder','Rights Holder'),
  ('agency','Agency'),('media_outlet','Media Outlet'),('service','Service'),
  ('school','School'),('university','University'),('grassroots','Grassroots'),
  ('company','Company')
on conflict (code) do nothing;

-- company_org_types / competitions / org_relationships (gk_orgs) ---------------
create table if not exists public.company_org_types (
  company_id bigint not null references public.companies(id) on delete cascade,
  org_type_id bigint not null references public.org_types(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (company_id, org_type_id)
);

create table if not exists public.competitions (
  company_id bigint primary key references public.companies(id) on delete cascade,
  level text, format text, season text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.org_relationships (
  id bigint generated by default as identity primary key,
  from_company_id bigint not null references public.companies(id) on delete cascade,
  to_company_id bigint not null references public.companies(id) on delete cascade,
  relationship_type text not null,
  region text,
  is_current boolean not null default true,
  start_date date, end_date date,
  created_at timestamptz not null default now(),
  constraint org_relationships_type_check check (relationship_type in ('parent_of','governs','broadcasts','affiliated_with','owns')),
  constraint org_relationships_no_self_check check (from_company_id <> to_company_id),
  constraint org_relationships_date_order_check check (start_date is null or end_date is null or start_date <= end_date)
);
create unique index if not exists org_relationships_active_uniq
  on public.org_relationships (from_company_id, to_company_id, relationship_type) where (is_current);

-- clubs columns (gk_orgs) ------------------------------------------------------
alter table public.clubs add column if not exists league_company_id bigint references public.companies(id);
alter table public.clubs add column if not exists gps_org_id bigint;

-- RLS + grants for the (re)created tables --------------------------------------
do $$ declare t text; begin
  foreach t in array array['org_types','company_org_types','competitions','org_relationships'] loop
    execute format('alter table public.%I enable row level security', t);
    execute format('drop policy if exists "authenticated full access" on public.%I', t);
    execute format('create policy "authenticated full access" on public.%I for all to authenticated using (true) with check (true)', t);
    execute format('grant all on public.%I to anon, authenticated, service_role', t);
  end loop;
end $$;
grant usage, select on all sequences in schema public to anon, authenticated, service_role;
